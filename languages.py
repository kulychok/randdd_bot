language = 'en'


def get_language(lang_code):
    if not lang_code:
        return 'en'
    if '-' in lang_code:
        lang_code = lang_code.split("-")[0]
    if lang_code == 'ru':
        return 'ru'
    if lang_code == 'uk':
        return 'ua'
    else:
        return 'en'


